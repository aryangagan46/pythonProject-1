'********* Different Data Types *********'

name = "Darshan"                 # String data Type
print(name)

reg_number = 15                    # Integer data type
print(reg_number)

percentage_obtained = 64.70        # Float Data Type
print(percentage_obtained)

marks_obtained_list = [72, 55, 86, 74, 80, 94]  # list Data type
print(marks_obtained_list)

student_details_tuple = ('Darshan', 65, 15)     # tuple Data type
print(student_details_tuple)

student_dictionary = {'name': 'Darshan','reg_number': 15, 'percentage' : 65}  # dictionary data type
print(student_dictionary)

student_exists = True              # Boolean data type
print(student_exists)

student_percentage = float(65)      # data type conversion
print(student_percentage)
